// switch is a great replacement to long else if construct
# include<stdio.h>
int main(){
    int day;
    printf(" enter a day from (0-6  :)");
    scanf("%d",&day);
    switch(day){
        case 0: printf("monday");
        break;
        case 1: printf("monday");
        break;
        case 2: printf("monday");
        break;
        case 3: printf("monday");
        break;
        case 4: printf("monday");
        break;
        case 5: printf("monday");
        break;
        case 6: printf("monday");
        break;
        default : printf(" invalid statement");
    }
    return 0;
}