// TO AVOID THE REPEATATING CODING  WE USE LOOPS -TO IMPLEMENT LOOPING CONSTRUCT KEYWORDS AVAILLIABLE ARE  - WHILE  | DO-WHILE| FOR
// IF THE CONDITION EVALUATE ARE TRUE THEN CONTROL LOOP ME JAAYEGA - IF CONDITION FALSE LOOP SE BYPASS KAR JAYEGA
# include<stdio.h>
int main()
{
    int x =1;
    while(x<=5){
        printf("%d \n ",x); // x++ first then output - 2,3,4,5,6
        x++; // output - 1,2,3,4,5
    }
    return 0;
}

